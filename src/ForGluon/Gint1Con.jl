                    9216 * (-4 + r2) * cosrP7 * (
                        12288 + 26624 * r2 + 26880 * r4 - 3840 * r6 -
                        1840 * r8 + 136 * r10 + 7 * r12 + (
                            -36864 - 61440 * r2 - 37120 * r4 + 2048 * r6 -
                            2480 * r8 + 240 * r10 + 21 * r12
                        ) * cosr +
                        3 * (4 + r2)^3 *
                            (192 + 80 * r2 - 60 * r4 + 7 * r6) * cosrP2 +
                        (-16 + r4)^2 * (-48 - 32 * r2 + 7 * r4) * cosrP3
                    ) * log((-4 + r2)/(4 + r2) + 0 * im)
                )
            )/(4 + r2)^9 - (
                12 * (1 + cosr)^2 * (
                    105 * (4 + r2)^3 +
                    45 * (4 + r2)^2 * (-36 + 5 * r2) * cosr +
                    10 * (2368 - 912 * r2 - 300 * r4 + 19 * r6) * cosrP2 +
