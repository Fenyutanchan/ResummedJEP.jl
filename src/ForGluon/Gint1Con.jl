                    2 * (-14400 + 6768 * r2 - 516 * r4 + 35 * r6) * cosrP5 +
                    (-384 + 2784 * r2 + 360 * r4 - 154 * r6) * cosrP6 -
                    6 * (448 - 208 * r2 - 4 * r4 + 7 * r6) * cosrP7 +
                    (-4 + r2)^2 * (20 + r2) * cosrP8 +
                    (-4 + r2)^3 * cosrP9
                ) * log(1 - cosr)
            )/(4 + r2)^3
        ) * secrP8
    )/(4608 * ej * pi * (1 + ((-4 + r2) * cosr)/(4 + r2))^3)

    return  result
end

