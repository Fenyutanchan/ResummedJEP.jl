    r4  =   r^4
    r6  =   r^6
    r8  =   r^8
    r10 =   r^10
    r12 =   r^12
    r14 =   r^14

    cosr    =   cos(r)
    cosrP2  =   cosr^2
    cosrP3  =   cosr^3
    cosrP4  =   cosr^4
    cosrP5  =   cosr^5
    cosrP6  =   cosr^6
    cosrP7  =   cosr^7
    cosrP8  =   cosr^8
    cosrP9  =   cosr^9

    secrP8  =   sec(r)^8

    result    =  (
        AlphaS * CA * (
            (
                cosr * (
                    - (
                        (4 + r2) * (4 + r2 + (-4 + r2) * cosr) *
                            (1260 * (4 + r2)^7 +
                        90 * (4 + r2)^6 * (-20 + 51 * r2) * cosr +
                        180 * (4 + r2)^5 * (-464 - 128 * r2 + 41 * r4) *
                            cosrP2 +
                        9 * (4 + r2)^4 *
                            (6464 - 22160 * r2 - 2852 * r4 + 1029 * r6) *
                            cosrP3 +
                        2 * (4 + r2)^3 * (
                            876800 + 388736 * r2 + 33792 * r4 + 28760 * r6 +
                            5423 * r8
                        ) * cosrP4 +
                        (4 + r2)^2 * (
                            -508928 + 1816320 * r2 + 857216 * r4 +
                            594976 * r6 + 121980 * r8 + 8327 * r10
                        ) * cosrP5 +
                        8 * (-6406144 - 5885952 * r2 - 3490816 * r4 +
                        323840 * r6 + 552896 * r8 + 117968 * r10 +
                        10980 * r12 + 383 * r14) * cosrP6 +
                        48 * (-4 + r2) * (4 + r2)^3 * cosrP8 * (
                            r6 * (-51 + 43 * log(2)) +
                            32 * (-25 + 86 * log(2)) +
                            16 * r2 * (56 + 129 * log(2)) +
                            r4 * (-506 + 516 * log(2))
                        ) +
                        6 * cosrP7 * (r14 * (-135 + 172 * log(2)) +
                        16384 * (-11 + 172 * log(2)) +
                        1280 * r6 * (-1713 + 1204 * log(2)) +
                        4 * r12 * (-653 + 1204 * log(2)) +
                        320 * r8 * (243 + 1204 * log(2)) +
                        4096 * r2 * (463 + 1204 * log(2)) +
                        3072 * r4 * (1971 + 1204 * log(2)) +
                        16 * r10 * (-659 + 3612 * log(2))) +
                        6 * (-16 + r4)^2 * cosrP9 *
                        (r6 * (-211 + 172 * log(2)) +
                        12 * r4 * (-171 + 172 * log(2)) +
                        48 * r2 * (77 + 172 * log(2)) +
                        2752 * (-1 + log(16))))
                    ) +
                    9216 * (-4 + r2) * cosrP7 * (
                        12288 + 26624 * r2 + 26880 * r4 -
                        3840 * r6 - 1840 * r8 +
                        136 * r10 + 7 * r12 + (
                            -36864 - 61440 * r2 - 37120 * r4 + 2048 * r6 -
                            2480 * r8 + 240 * r10 + 21 * r12
                        ) * cosr +
                        3 * (4 + r2)^3 *
                            (192 + 80 * r2 - 60 * r4 + 7 * r6) * cosrP2 +
                        (-16 + r4)^2 * (-48 - 32 * r2 + 7 * r4) *
                            cosrP3
